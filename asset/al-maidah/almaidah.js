// State Management
let state = {
  showArabic: true,
  showRumi: true,
  showTranslation: true,
  darkMode: false,
  bookmarks: JSON.parse(localStorage.getItem('almaidah_bookmarks') || '[]'),
  currentPlayingAyah: null,
  audioElement: null,
  readAyahs: new Set(JSON.parse(localStorage.getItem('almaidah_read') || '[]'))
};

// Initialize
document.addEventListener('DOMContentLoaded', function() {
  renderAyahs();
  updateStats();
  attachEventListeners();
  loadUserPreferences();
});

function renderAyahs(filteredData = null) {
  const ayahList = document.getElementById('ayahList');
  const data = filteredData || surahData.ayahs;
  
  ayahList.innerHTML = data.map(ayah => {
    const isBookmarked = state.bookmarks.some(b => b.number === ayah.number);
    const bookmarkNote = state.bookmarks.find(b => b.number === ayah.number)?.note || '';
    
    return `
      <div class="ayah-card" data-ayah="${ayah.number}" id="ayah-${ayah.number}">
        <div class="ayah-header">
          <span class="ayah-number">Ayat ${ayah.number}</span>
          <div class="ayah-actions">
            <button class="icon-btn play-ayah-btn" data-ayah="${ayah.number}" title="Main audio">
              ▶️
            </button>
            <button class="icon-btn bookmark-btn ${isBookmarked ? 'active' : ''}" data-ayah="${ayah.number}" title="Bookmark">
              ${isBookmarked ? '🔖' : '📑'}
            </button>
            <button class="icon-btn copy-btn" data-ayah="${ayah.number}" title="Salin">
              📋
            </button>
            <button class="icon-btn share-ayah-btn" data-ayah="${ayah.number}" title="Share">
              📤
            </button>
          </div>
        </div>
        
        <div class="ayah-arabic ${!state.showArabic ? 'hidden' : ''}">
          ${ayah.arabic}
        </div>
        
        <div class="ayah-rumi ${!state.showRumi ? 'hidden' : ''}">
          ${ayah.rumi}
        </div>
        
        <div class="ayah-translation ${!state.showTranslation ? 'hidden' : ''}">
          ${ayah.translation}
        </div>
        
        ${isBookmarked && bookmarkNote ? `
          <div class="bookmark-note">
            📝 <strong>Nota:</strong> ${bookmarkNote}
          </div>
        ` : ''}
      </div>
    `;
  }).join('');
  
  // Re-attach event listeners for dynamically created elements
  attachAyahEventListeners();
}

function attachEventListeners() {
  // Toggle controls
  document.getElementById('toggleArabic').addEventListener('click', function() {
    state.showArabic = !state.showArabic;
    this.classList.toggle('active');
    document.querySelectorAll('.ayah-arabic').forEach(el => el.classList.toggle('hidden'));
  });
  
  document.getElementById('toggleRumi').addEventListener('click', function() {
    state.showRumi = !state.showRumi;
    this.classList.toggle('active');
    document.querySelectorAll('.ayah-rumi').forEach(el => el.classList.toggle('hidden'));
  });
  
  document.getElementById('toggleTranslation').addEventListener('click', function() {
    state.showTranslation = !state.showTranslation;
    this.classList.toggle('active');
    document.querySelectorAll('.ayah-translation').forEach(el => el.classList.toggle('hidden'));
  });
  
  document.getElementById('toggleDarkMode').addEventListener('click', function() {
    state.darkMode = !state.darkMode;
    document.getElementById('surahContainer').classList.toggle('dark-mode');
    localStorage.setItem('almaidah_darkmode', state.darkMode);
  });
  
  // Search functionality
  document.getElementById('searchAyah').addEventListener('input', function(e) {
    const query = e.target.value.toLowerCase().trim();
    
    if (!query) {
      renderAyahs();
      return;
    }
    
    const filtered = surahData.ayahs.filter(ayah => {
      const ayahNum = query.match(/\d+/);
      if (ayahNum) {
        return ayah.number === parseInt(ayahNum[0]);
      }
      return ayah.translation.toLowerCase().includes(query) ||
             ayah.rumi.toLowerCase().includes(query);
    });
    
    renderAyahs(filtered);
  });
  
  // Show bookmarks
  document.getElementById('showBookmarks').addEventListener('click', function() {
    if (state.bookmarks.length === 0) {
      showToast('Tiada ayat yang di-bookmark lagi');
      return;
    }
    
    const bookmarkedAyahs = surahData.ayahs.filter(ayah => 
      state.bookmarks.some(b => b.number === ayah.number)
    );
    renderAyahs(bookmarkedAyahs);
    showToast(`Memaparkan ${bookmarkedAyahs.length} ayat yang di-bookmark`);
  });
  
  // Share button
  document.getElementById('shareBtn').addEventListener('click', function() {
    const shareData = {
      title: 'Surah Al Maidah - Platform Interaktif',
      text: 'Baca Surah Al Maidah dengan audio sync, transliterasi Rumi & terjemahan Melayu lengkap!',
      url: window.location.href
    };
    
    if (navigator.share) {
      navigator.share(shareData);
    } else {
      navigator.clipboard.writeText(window.location.href);
      showToast('Link disalin ke clipboard!');
    }
  });
  
  // Main audio player
  document.getElementById('playBtn').addEventListener('click', function() {
    if (!state.audioElement) {
      playAyah(1);
    } else {
      if (state.audioElement.paused) {
        state.audioElement.play();
        document.getElementById('playIcon').textContent = '⏸️';
      } else {
        state.audioElement.pause();
        document.getElementById('playIcon').textContent = '▶️';
      }
    }
  });
}

function attachAyahEventListeners() {
  // Play individual ayah
  document.querySelectorAll('.play-ayah-btn').forEach(btn => {
    btn.addEventListener('click', function() {
      const ayahNum = parseInt(this.dataset.ayah);
      playAyah(ayahNum);
    });
  });
  
  // Bookmark ayah
  document.querySelectorAll('.bookmark-btn').forEach(btn => {
    btn.addEventListener('click', function() {
      const ayahNum = parseInt(this.dataset.ayah);
      toggleBookmark(ayahNum);
    });
  });
  
  // Copy ayah
  document.querySelectorAll('.copy-btn').forEach(btn => {
    btn.addEventListener('click', function() {
      const ayahNum = parseInt(this.dataset.ayah);
      copyAyah(ayahNum);
    });
  });
  
  // Share individual ayah
  document.querySelectorAll('.share-ayah-btn').forEach(btn => {
    btn.addEventListener('click', function() {
      const ayahNum = parseInt(this.dataset.ayah);
      shareAyah(ayahNum);
    });
  });
}

function playAyah(ayahNumber) {
  const ayah = surahData.ayahs.find(a => a.number === ayahNumber);
  if (!ayah) return;
  
  // Stop previous audio
  if (state.audioElement) {
    state.audioElement.pause();
    document.querySelectorAll('.ayah-card').forEach(card => card.classList.remove('playing'));
  }
  
  // Create new audio
  state.audioElement = new Audio(ayah.audioUrl);
  state.currentPlayingAyah = ayahNumber;
  
  // Update UI
  document.getElementById('currentAyahText').textContent = `Main: Ayat ${ayahNumber}`;
  document.getElementById('playIcon').textContent = '⏸️';
  document.getElementById(`ayah-${ayahNumber}`).classList.add('playing');
  
  // Mark as read
  state.readAyahs.add(ayahNumber);
  localStorage.setItem('almaidah_read', JSON.stringify([...state.readAyahs]));
  updateStats();
  
  // Play audio
  state.audioElement.play();
  
  // Update progress bar
  state.audioElement.addEventListener('timeupdate', function() {
    const progress = (this.currentTime / this.duration) * 100;
    document.getElementById('progressFill').style.width = progress + '%';
  });
  
  // Auto play next ayah
  state.audioElement.addEventListener('ended', function() {
    const nextAyah = ayahNumber + 1;
    if (nextAyah <= surahData.ayahs.length) {
      playAyah(nextAyah);
    } else {
      document.getElementById('playIcon').textContent = '▶️';
      document.getElementById('currentAyahText').textContent = 'Tamat - Tekan play untuk ulang';
      document.querySelectorAll('.ayah-card').forEach(card => card.classList.remove('playing'));
    }
  });
  
  // Scroll to ayah
  document.getElementById(`ayah-${ayahNumber}`).scrollIntoView({ behavior: 'smooth', block: 'center' });
}

function toggleBookmark(ayahNumber) {
  const existingIndex = state.bookmarks.findIndex(b => b.number === ayahNumber);
  
  if (existingIndex > -1) {
    state.bookmarks.splice(existingIndex, 1);
    showToast('Bookmark dibuang');
  } else {
    const note = prompt('Tambah nota untuk ayat ini (optional):');
    state.bookmarks.push({
      number: ayahNumber,
      note: note || '',
      timestamp: new Date().toISOString()
    });
    showToast('Ayat di-bookmark!');
  }
  
  localStorage.setItem('almaidah_bookmarks', JSON.stringify(state.bookmarks));
  updateStats();
  renderAyahs();
}

function copyAyah(ayahNumber) {
  const ayah = surahData.ayahs.find(a => a.number === ayahNumber);
  if (!ayah) return;
  
  const text = `
Surah Al Maidah - Ayat ${ayahNumber}

${ayah.arabic}

${ayah.rumi}

${ayah.translation}

Sumber: ${window.location.href}
  `.trim();
  
  navigator.clipboard.writeText(text);
  showToast('Ayat disalin ke clipboard!');
}

function shareAyah(ayahNumber) {
  const ayah = surahData.ayahs.find(a => a.number === ayahNumber);
  if (!ayah) return;
  
  const shareData = {
    title: `Surah Al Maidah - Ayat ${ayahNumber}`,
    text: `${ayah.translation}\n\n`,
    url: `${window.location.href}#ayah-${ayahNumber}`
  };
  
  if (navigator.share) {
    navigator.share(shareData);
  } else {
    copyAyah(ayahNumber);
  }
}

function updateStats() {
  document.getElementById('totalAyahs').textContent = surahData.ayahs.length;
  document.getElementById('readCount').textContent = state.readAyahs.size;
  document.getElementById('bookmarkedCount').textContent = state.bookmarks.length;
  document.getElementById('bookmarkCount').textContent = state.bookmarks.length;
}

function showToast(message) {
  const toast = document.createElement('div');
  toast.className = 'toast';
  toast.textContent = message;
  document.body.appendChild(toast);
  
  setTimeout(() => {
    toast.remove();
  }, 3000);
}

function loadUserPreferences() {
  const savedDarkMode = localStorage.getItem('almaidah_darkmode') === 'true';
  if (savedDarkMode) {
    state.darkMode = true;
    document.getElementById('surahContainer').classList.add('dark-mode');
  }
}
